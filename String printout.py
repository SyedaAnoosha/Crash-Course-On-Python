# The following 3 lines assign strings to a list of variables.

first_name = "Syeda"
middle_name = "Anoosha"
last_name = "Iqtidar"
 
print(first_name + " " + middle_name + " " + last_name)
 
# Alternatively, you could use commas in place of the + connector:
print(first_name, middle_name, last_name)

