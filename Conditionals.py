# ==    (equality)  
# !=     (not equal to)  
# >       (greater than)
# <      (less than)
# >=    (greater than or equal to)
# <=    (less than or equal to)

# Comparison Operators with Numbers
print(32 == 30+2) # True
print(9/3 != 3*1) # False

# Comparison Operators with Strings
print("a string" == "a string") #True

print("4 + 5" == 4 + 5) #False

event_city = "Hyd"
print(event_city != "Hyd") #False
